CREATE TABLE NEWEMPTABLE
(
EMPLOYEE_ID NUMBER(6),
EMPLOYEE_NAME VARCHAR(20)
);
SELECT * FROM NEWEMPTABLE;


CREATE OR REPLACE PROCEDURE "NEWEMPTABLEPROC" (
EMPLOYEE_ID IN NEWEMPTABLE.EMPLOYEE_ID%TYPE,
EMPLOYEE_NAME IN NEWEMPTABLE.EMPLOYEE_NAME%TYPE
)
IS
BEGIN
    INSERT INTO NEWEMPTABLE VALUES (EMPLOYEE_ID,EMPLOYEE_NAME);
    COMMIT;
END;